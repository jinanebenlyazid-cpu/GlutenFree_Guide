function updateLang($lang, $add) {
    $file = "lang/{$lang}.json";
    $json = json_decode(file_get_contents($file), true);
    $merged = array_merge($json, $add);
    file_put_contents($file, json_encode($merged, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE));
}

